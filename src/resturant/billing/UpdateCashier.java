    void show_table(){
        
        String query = "Select * from resturant.dbo.cashier"; 
        
        try{
                dbc = new DBConnect();
                dbc.connectToDB(); 
                try{
                    java.sql.Statement stmt= dbc.connection.createStatement();
                    rs = stmt.executeQuery(query);
                
                    jTable1.setModel(DbUtils.resultSetToTableModel(rs));
                    
                    TableColumnModel tcm = jTable1.getColumnModel();
                    tcm.getColumn(0).setHeaderValue("Cashier's ID");
                    tcm.getColumn(1).setHeaderValue("Password");
                    tcm.getColumn(2).setHeaderValue("First Name");
                    tcm.getColumn(3).setHeaderValue("Last Name");
                    tcm.getColumn(4).setHeaderValue("Mobile Number");
                    tcm.getColumn(5).setHeaderValue("Under Manager ID");
                    
                    jTable1.setRowHeight(25);
                    
                }catch (SQLException ex){
                    JOptionPane.showMessageDialog(null, "Unable to exicute");
                    Logger.getLogger(DBConnect.class.getName()).log(Level.SEVERE, null, ex);  
                }  
                dbc.disconnectFromDB();            
            }catch (SQLException ex) {
                System.out.println(ex.getMessage());   
            }catch (ClassNotFoundException ex) {
                Logger.getLogger(login.class.getName()).log(Level.SEVERE, null, ex);
            }finally {
                closeDB();
            }
    }

